        ),
      ),
      resizeToAvoidBottomInset: false,
      backgroundColor: notifire.getwhite,
      body: Container(
        decoration: BoxDecoration(
          image: !useBackgroundImage
              ? const DecorationImage(
                  image: ExactAssetImage('images/bg.png'),
                  fit: BoxFit.fill,
                )
              : null,
